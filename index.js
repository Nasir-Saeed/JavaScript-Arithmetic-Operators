// Arithmetic Operator in Javascript

// In this video we will see five Arithmetic Operators

// 1: Addition +
// 2: Subtraction -
// 3: Multiplication: *
// 4: Division: /
// 5: Modulus: %


let a = 20;
let b = 24;

console.log("The + of two numbers: ", a + b); // 20 + 24
console.log("The - of two numbers: ", a - b); // 20 - 24
console.log("The * of two numbers: ", a * b); // 20 * 24
console.log("The / of two numbers: ", a / b); // 20 / 24
console.log("The % of two numbers: ", a % b); // 20 % 24

